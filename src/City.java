public class City {
    private String name;
    private int area;
    private int population;

    public void setName(String name) {
        this.name = name;

    }

    public void setArea(int area) {
        this.name = name;
    }

    public void setPopulation(int population) {
        this.population = population;
    }

    public String getName() {
        return name;
    }


    public int getArea() {
        return area;
    }


    public int getPopulation() {
        return population;
    }

}
